import java.util.Scanner;

public class Calculations {
    public static void main(String[] args) {
       sum(4  , 4);
       extract(4  , 4);
       divide(4  , 4);
       multiply(4  , 4);

    }

    public static int sum(int num1, int num2){return num1 + num2;}
    public static int extract(int num1, int num2){return num1 - num2;}
    public static int divide(int num1, int num2){return num1 / num2;}
    public static int multiply(int num1, int num2){return num1 * num2;}

}